"""Unit tests for the PTX ISA ``.version`` downgrade helper (#3795).

The driver-JIT retry itself (cudaErrorUnsupportedPtxVersion -> downgrade ->
reload) cannot be pinned hermetically: whether a driver rejects a
newer-minor ``.version`` header is a property of the driver build (a CUDA
13.0 driver on this machine accepts even ``.version 99.0`` through
``cuLibraryLoadData``), so error 222 cannot be forced from the PTX text
alone. These tests pin the deterministic half: the header rewrite the retry
relies on.
"""

from flashinfer.gdn_kernels.delta_rule_dsl.custom_compile_cache import (
    _downgrade_ptx_isa_version,
)

PTX_TEMPLATE = """\
//
// Generated by NVIDIA NVVM Compiler 9.1
//
.version {version}
.target sm_120a
.address_size 64

.visible .entry noop_kernel()
{{
    // version 9.1 in a comment must not be rewritten
    ret;
}}
"""


def test_downgrade_rewrites_only_the_header_minor():
    ptx = PTX_TEMPLATE.format(version="9.1")
    downgraded = _downgrade_ptx_isa_version(ptx)
    assert downgraded is not None
    assert ".version 9.0" in downgraded
    assert ".version 9.1" not in downgraded
    # Everything except the header line is untouched, including the
    # "9.1" occurrences in comments.
    assert downgraded.replace(".version 9.0", ".version 9.1", 1) == ptx


def test_downgrade_returns_none_at_baseline_minor():
    assert _downgrade_ptx_isa_version(PTX_TEMPLATE.format(version="9.0")) is None


def test_downgrade_returns_none_without_version_line():
    ptx = PTX_TEMPLATE.format(version="9.1").replace(".version 9.1\n", "")
    assert _downgrade_ptx_isa_version(ptx) is None


def test_downgrade_handles_multi_digit_minor():
    downgraded = _downgrade_ptx_isa_version(PTX_TEMPLATE.format(version="10.13"))
    assert downgraded is not None
    assert ".version 10.0" in downgraded
